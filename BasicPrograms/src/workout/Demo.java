package workout;

import java.util.Scanner;
public class Demo {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("enter the a value:");
		int a=sc.nextInt();
		System.out.println("enter the b value:");
		int b=sc.nextInt();
		System.out.println("a+b="+(a+b));
		System.out.println("enter the c value:");
		int c=sc.nextInt();
		System.out.println("enter the d value:");
		int d=sc.nextInt();
		System.out.println("c-d="+(c-d));
		System.out.println("enter the e value:");
		int e=sc.nextInt();
		System.out.println("enter the f value:");
		int f=sc.nextInt();
		System.out.println("e*f="+(e*f));
		System.out.println("enter the g value:");
		int g=sc.nextInt();
		System.out.println("enter the h value:");
		int h=sc.nextInt();
		System.out.println("g/h="+(g/h));
		System.out.println("enter the i value:");
		int i=sc.nextInt();
		System.out.println("enter the j value:");
		int j=sc.nextInt();
		System.out.println("i%j="+(i%j));
		
	}
}

