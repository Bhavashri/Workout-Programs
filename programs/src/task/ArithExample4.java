public class ArithExample4 {
	public int add(int a,int b) {
		 return a+b;
	 }
	 public int sub(int a,int b) {
		return a-b; 
	 }
	 public int multiple(int a,int b) {
		 return a*b;
	 }
	 public int division(int a,int b) {
		return a/b; 
	 }
	 public int modulo(int a,int b) {
		 return a%b;
	 }
	
}
